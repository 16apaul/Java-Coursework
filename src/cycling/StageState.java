package cycling;

    /**
 * This enum is used to represent the stage states  on road races.
 * 
 * @author Aritra
 * @version 1.0
 *
 */
public enum StageState {
	
	/**
	 * Used when stage is created
	 */
	ADDING_CHECKPOINTS,
	PREPARATION,
	/**
	 * When stage is concluded
	 */
	WAITING_FOR_RESULTS,
	
	
}


